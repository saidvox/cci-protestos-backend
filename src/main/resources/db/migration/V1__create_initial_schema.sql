CREATE INDEX idx_carga_filas_carga ON carga_excel_filas(carga_id);
CREATE INDEX idx_carga_filas_estado ON carga_excel_filas(estado);
CREATE INDEX idx_deudores_documento ON deudores(numero_documento);
CREATE INDEX idx_deudores_nombre ON deudores(nombre_razon_social);
CREATE INDEX idx_documentos_solicitud ON documentos(solicitud_id);
CREATE INDEX idx_documentos_tramite_activo ON documentos_tramite(activo);
CREATE INDEX idx_documentos_tramite_orden ON documentos_tramite(orden);
CREATE INDEX idx_protestos_deudor ON protestos(deudor_id);
CREATE INDEX idx_protestos_entidad ON protestos(entidad_id);
